  -- 6. ROW LEVEL SECURITY (permissive for MVP)
  -- =============================================
ALTER TABLE
  public.users ENABLE ROW LEVEL SECURITY;
ALTER TABLE
  public.tests ENABLE ROW LEVEL SECURITY;
ALTER TABLE
  public.questions ENABLE ROW LEVEL SECURITY;
ALTER TABLE
  public.responses ENABLE ROW LEVEL SECURITY;
