-- Run this script in your Supabase SQL Editor to initialize the database
-- =============================================
-- 1. USERS TABLE (Custom Auth)
-- =============================================
CREATE TABLE IF NOT EXISTS public.users (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  username TEXT NOT NULL UNIQUE,
  "fullName" TEXT NOT NULL,
  password TEXT NOT NULL,
  role TEXT NOT NULL DEFAULT 'student',
  gender TEXT,
  region TEXT,
  "avatarUrl" TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc' :: text, now()) NOT NULL
);
-- =============================================
-- 2. TESTS TABLE (groups questions together)
-- =============================================
CREATE TABLE IF NOT EXISTS public.tests (
  id SERIAL PRIMARY KEY,
  title TEXT NOT NULL,
  description TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc' :: text, now()) NOT NULL
);
-- =============================================
-- 3. QUESTIONS TABLE (belongs to a test)
-- =============================================
CREATE TABLE IF NOT EXISTS public.questions (
  id SERIAL PRIMARY KEY,
  test_id INTEGER NOT NULL REFERENCES public.tests(id) ON DELETE CASCADE,
  q_text TEXT NOT NULL,
  part TEXT NOT NULL,
  image TEXT,
  speaking_timer INTEGER NOT NULL DEFAULT 30,
  prep_timer INTEGER NOT NULL DEFAULT 5,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc' :: text, now()) NOT NULL
);
-- =============================================
-- 4. RESPONSES TABLE (student answer to a question)
-- =============================================
CREATE TABLE IF NOT EXISTS public.responses (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  question_id INTEGER NOT NULL REFERENCES public.questions(id) ON DELETE CASCADE,
  student_id UUID NOT NULL REFERENCES public.users(id) ON DELETE CASCADE,
  local_uri TEXT,
  remote_url TEXT,
  teacher_score INTEGER,
  teacher_feedback TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc' :: text, now()) NOT NULL
);
