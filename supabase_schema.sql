CREATE POLICY "Allow all operations for users" ON public.users FOR ALL USING (true) WITH CHECK (true);
DROP POLICY IF EXISTS "Allow all operations for tests" ON public.tests;
CREATE POLICY "Allow all operations for tests" ON public.tests FOR ALL USING (true) WITH CHECK (true);
DROP POLICY IF EXISTS "Allow all operations for questions" ON public.questions;
CREATE POLICY "Allow all operations for questions" ON public.questions FOR ALL USING (true) WITH CHECK (true);
DROP POLICY IF EXISTS "Allow all operations for responses" ON public.responses;
CREATE POLICY "Allow all operations for responses" ON public.responses FOR ALL USING (true) WITH CHECK (true);
-- =============================================
  -- 7. GROUPS TABLE
  -- =============================================
  CREATE TABLE IF NOT EXISTS public.groups (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    description TEXT,
    teacher_id UUID NOT NULL REFERENCES public.users(id) ON DELETE CASCADE,
    referral_code TEXT NOT NULL UNIQUE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc' :: text, now()) NOT NULL
  );
-- =============================================
  -- 8. GROUP MEMBERS TABLE
  -- =============================================
  CREATE TABLE IF NOT EXISTS public.group_members (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id UUID NOT NULL REFERENCES public.groups(id) ON DELETE CASCADE,
    student_id UUID NOT NULL REFERENCES public.users(id) ON DELETE CASCADE,
    joined_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc' :: text, now()) NOT NULL,
    UNIQUE(group_id, student_id)
  );
